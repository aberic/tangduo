#!/bin/bash

echo build start ===========================================================================================================

# mvn protobuf:compile -f rpc/pom.xml
# mvn protobuf:compile-custom -f rpc/pom.xml

mvnInstall() {
  echo install "$1" ========================================================================================================
  mvn -f "$1"/pom.xml -DskipTests=true install
}

mvnPackage() {
  echo clean package "$1" ===================================================================================================
  mvn -f "$1"/pom.xml -DskipTests=true clean package
  cp "$1"/target/"$1".jar build
}

rm -rf build/*.jar
echo clean tangduo start ====================================================================================================
mvn -f pom.xml -DskipTests=true clean
echo clean tangduo end ======================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================

echo install tangduo start ==================================================================================================
mvn -f pom.xml -DskipTests=true install
echo install tangduo end ====================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================

echo mvn package start ======================================================================================================
for loop in search
do
    mvnPackage $loop
done
echo mvn package end ========================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================
echo ========================================================================================================================

echo build end ==============================================================================================================